package nov24;

public class SwitchStatement {

    public static void main(String[] args) {

        // conditional statements

        // switch

//        boolean b =true;

        String b = "fedfdf";

        switch(b) {

            case "dfd":
                System.out.println("Hello");
                break;
            case "fedfdf":
                System.out.println("Hi");
                break;
            case "tops":
                System.out.println("Kem cho");
                break;
            default:
                System.out.println("Nothing");
        }

    }
}
