package nov24;

public class Loops {

    public static void main(String[] args) {


        // loop  // to decrease code size  // concise

        // 4 types

        // while // do..while // for  // for..each(advance for loop)
        ///////////////////////////  while  //////////////////////////
        int x =23;

        while(x<30){
            x++; //24 // 25 // 26 // 27 // 28 // 29 // 30
            System.out.println("Hello");

        }
        System.out.println("\n \n \n");

        //////////////////////// do.. while //////////////////////

        int y =10;   // runs at least one time

        do{
            System.out.println("Hello");
            y++;
        }while(y<20);

        //////////////////////////  for  ////////////////////////////////

        System.out.println("\n\n");

        for(int z =0;z<5;z++){

            System.out.println("Hello");
        }

        //////////////////////////// for each ////////////////////////////////



    }
}
